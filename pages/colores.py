class Colores:
    def __init__(self):
        self.AZUL_OSCURO = "#1995AD"
        self.AZUL_MEDIO = "#A1D6E2"
        self.AZUL_CLARO = "#BCBABE"
        self.AZUL_BLANCO = "#F1F1F2"

    def get_oscuro(self):
        return self.AZUL_OSCURO 
    
    def get_medio(self):
        return self.AZUL_MEDIO
    
    def get_claro(self):
        return self.AZUL_CLARO
    
    def get_blanco(self):
        return self.AZUL_BLANCO
    
colores = Colores()

